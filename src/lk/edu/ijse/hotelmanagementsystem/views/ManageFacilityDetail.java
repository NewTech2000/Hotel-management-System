    private void loadDataFacility() {

        dtmFac.setRowCount(0);
        try {
            ArrayList<FacilityDTO> allFacility = ctrlFac.getAll();
            
            if (allFacility == null) {
                return;
            }
            for (FacilityDTO room : allFacility) {
                
                Object[] rowData = {
                    room.getFid(),
                    room.getFacility_Name(),
                    room.getPrices()
                };
                dtmFac.addRow(rowData);
            }
        } catch (Exception ex) {
            Logger.getLogger(ManageFacilityDetail.class.getName()).log(Level.SEVERE, null, ex);
        }
    }


