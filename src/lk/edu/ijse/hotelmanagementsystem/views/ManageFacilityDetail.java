    private void searchFacility() {
        String fid = txtFID.getText();
        
        try{
            FacilityDTO facList = ctrlFac.search(fid);
            if (facList== null) {
                JOptionPane.showMessageDialog(this, "Facility not found..");
            } else {
                txtFID.setText(facList.getFid());
                txtFacilityName.setText(facList.getFacility_Name());
                txtPrices.setText(""+facList.getPrices());
            }
        }catch(Exception e){
            Logger.getLogger(ManageFacilityDetail.class.getName()).log(Level.SEVERE, null, e);
        }
    }
    
    private void addFacility() {
        
        String fid = txtFID.getText();
        String FacilityName = txtFacilityName.getText();
        Double price =  Double.parseDouble(txtPrices.getText());
       
        FacilityDTO fac = new FacilityDTO(fid, FacilityName, price);
        try {
            boolean add = ctrlFac.add(fac);
            if (add) {
                JOptionPane.showMessageDialog(this, "Facility Added Successfully");
            }else{
                JOptionPane.showMessageDialog(this, "Facility Added Failed");
            }
        } catch (Exception e) {
        }
        loadDataFacility();
    }
    
    public void deleteFacility(){
        
        String fa = dtmFac.getValueAt(tblFacility.getSelectedRow(), 0).toString();
        try {
            boolean deleted = ctrlFac.delete(fa);
        } catch (Exception ex) {
            
            Logger.getLogger(ManageFacilityDetail.class.getName()).log(Level.SEVERE, null, ex);
        }
        loadDataFacility();
    }
    
    public void updateFacility(){
        
        String fid=txtFID.getText();
	String name=txtFacilityName.getText();
	Double prices=Double.parseDouble(txtPrices.getText());
        
        FacilityDTO fac = new FacilityDTO(fid, name, prices);
        try {
            boolean isUpdated = ctrlFac.update(fac);
            if (isUpdated) {
                JOptionPane.showMessageDialog( this,"Update Sucess");
            } else {
                JOptionPane.showMessageDialog( this,"Update SucessFully");
            }
            
        } catch (Exception e) {
            Logger.getLogger(ManageFacilityDetail.class.getName()).log(Level.SEVERE, null, e);
        }
        loadDataFacility();
    }
    private void tblClik() {
        
        int selectedRow = tblFacility.getSelectedRow();
        String rid = tblFacility.getValueAt(selectedRow, 0).toString();
        String name =tblFacility.getValueAt(selectedRow,1).toString();
        String price =tblFacility.getValueAt(selectedRow,2).toString();
        
        txtFID.setText(rid);
        txtFacilityName.setText(name);
        txtPrices.setText(price);
    }
    
    public void idGenerater(){
        try {
            String id= IDGenerator.getNewID("facility","fid", "F0");
            txtFID.setText(id);
        } catch (SQLException ex) {
            Logger.getLogger(ReceptionMain.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(ReceptionMain.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
